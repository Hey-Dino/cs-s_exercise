package cn.dino.demo.Demo0727.Demo03;

public class UseMath {
    public static void main(String[] args) {
        System.out.println("Math.abs(-11): " + Math.abs(-11));
        System.out.println();
        System.out.println("Math.ceil(1.4): " + Math.ceil(1.4));
        System.out.println("Math.ceil(-1.4): " + Math.ceil(-1.4));
        System.out.println();
        System.out.println("Math.floor(1.4): " + Math.floor(1.4));
        System.out.println("Math.floor(-1.4): " + Math.floor(-1.4));
        System.out.println();
        System.out.println("Math.round(1.5): " + Math.round(1.5));
        System.out.println("Math.round(-1.5): " + Math.round(-1.5));
        System.out.println("Math.round(-1.6): " + Math.round(-1.6));
        System.out.println();
        System.out.println("Math.pow(2,3): " + Math.pow(2, 3));
        System.out.println();
        System.out.println("Math.max(13.2223, 13.2222): " + Math.max(13.2223, 13.2222));
        System.out.println();
        System.out.println("Math.min(13.2223, 13.2222): " + Math.min(13.2223, 13.2222));
        System.out.println();
        System.out.println("Math.random(): " + Math.random());

        /* 还有诸如 sin()、cos()、tan()、asin()、acos()、atan()等静态方法 */
    }
}
