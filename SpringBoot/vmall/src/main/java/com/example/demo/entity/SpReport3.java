package com.example.demo.entity;


public class SpReport3 {

  private long id;
  private String rp3Src;
  private long rp3Count;
  private java.sql.Timestamp rp3Date;


  public long getId() {
    return id;
  }

  public void setId(long id) {
    this.id = id;
  }


  public String getRp3Src() {
    return rp3Src;
  }

  public void setRp3Src(String rp3Src) {
    this.rp3Src = rp3Src;
  }


  public long getRp3Count() {
    return rp3Count;
  }

  public void setRp3Count(long rp3Count) {
    this.rp3Count = rp3Count;
  }


  public java.sql.Timestamp getRp3Date() {
    return rp3Date;
  }

  public void setRp3Date(java.sql.Timestamp rp3Date) {
    this.rp3Date = rp3Date;
  }

}
